
def check_vowels():
    name = input("")
    name_lower = str(name).lower()
    print(f"Contiene a: {("a" in name_lower)}")
    print(f"Contiene e: {("e" in name_lower)}")
    print(f"Contiene i: {("i" in name_lower)}")
    print(f"Contiene o: {("o" in name_lower)}")
    print(f"Contiene u: {("u" in name_lower)}")
